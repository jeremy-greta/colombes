//SWITCH

let choix = 2;
switch (choix) {
    case 0:
        console.log("case 0");
    case 1:
        console.log("case 1");
    case 2:
        console.log("case 2");
    case 3:
        console.log("case 3");
    default:
        console.log("default");
}

switch (choix) {
    case 0:
        console.log("case 0");
        break;
    case 0:
        console.log("case 0");
        break;

    default:
        break;
}